#include <stdio.h>

int main() {
    int v1[4] = {1, 3, 5, 7}, v2[4] = {2, 4};
    int *ax, *ay;

    ax = &v1[2]; // ax => v1[2] = 5
    ay = &v2[2]; // ay => v2[2] = 0
    v2[2] = *(ax + 1); // v2[2] = 7 
    v2[3] = *ax; // v2[3] = 5
    ax = ax + 1; // ax => v1[3] = 7 
    v1[0] = *ax; // v1[0] = 7
    
    printf("v1[0] = %d | v1[1] = %d | v1[2] = %d | v1[3] = %d | v2[0] = %d | v2[1] = %d | v2[2] = %d | v2[3] = %d\n", v1[0], v1[1], v1[2], v1[3], v2[0], v2[1], v2[2], v2[3]);

    v1[2] = *ay; // v1[2] = 7
    v1[1] = --*ay; // v1[1] = 6 
    ax = ax + 1; // ax => v1[4] = trash
    v1[3] = *ax; //v1[3] = trash

    printf("v1[0] = %d | v1[1] = %d | v1[2] = %d | v1[3] = %d | v2[0] = %d | v2[1] = %d | v2[2] = %d | v2[3] = %d\n", v1[0], v1[1], v1[2], v1[3], v2[0], v2[1], v2[2], v2[3]);

    return 0;
}